def patch_run_card(card: Path, seed: int, cfg: Config) -> None:
    updates = {
        "nevents": str(cfg.nevents),
        "ebeam1": str(cfg.ebeam),
        "ebeam2": str(cfg.ebeam),
        "iseed": str(seed),
        "pdlabel": "lhapdf",
        "lhaid": str(LO_PDF_LHAID),
        "pta": str(cfg.gen_photon_pt_min),
        "etaa": str(cfg.gen_photon_eta_max),
        "ptj": str(cfg.gen_jet_pt_min),
        "etaj": str(cfg.gen_jet_eta_max),
        "ptl": str(cfg.gen_lepton_pt_min),
        "etal": str(cfg.gen_lepton_eta_max),
        "draa": str(cfg.gen_draa_min),
        "draj": str(cfg.gen_draj_min),
        "dral": str(cfg.gen_dral_min),
        "drjj": str(cfg.gen_drjj_min),
        "drll": str(cfg.gen_drll_min),
    }
    if cfg.dry_run:
        print(f"+ patch run card {card}")
        print(
            "nevents={nevents}, ebeam1={ebeam1}, ebeam2={ebeam2}, iseed={iseed}".format(**updates)
        )
        print("pdlabel={pdlabel}, lhaid={lhaid}".format(**updates))
        print(
            "pta={pta}, etaa={etaa}, ptj={ptj}, etaj={etaj}, "
            "ptl={ptl}, etal={etal}, draa={draa}, draj={draj}, dral={dral}, "
            "drjj={drjj}, drll={drll}".format(**updates)
        )
        return

    pattern = re.compile(r"^(\s*)([^=]+?)(\s*=\s*)([A-Za-z0-9_]+)(.*)$")
    output_lines: list[str] = []
    for line in card.read_text().splitlines(keepends=True):
        match = pattern.match(line)
        if match:
            indent, _value, sep, key, tail = match.groups()
            if key in updates:
                line = f"{indent}{updates[key]:>12}{sep}{key}{tail}\n"
        output_lines.append(line)
    card.write_text("".join(output_lines))


def rewrite_run_card_include(text: str) -> str:
    updates = {
        "PDLABEL": "'lhapdf'",
        "PDSUBLABEL(1)": "'lhapdf'",
        "PDSUBLABEL(2)": "'lhapdf'",
        "LHAID": str(LO_PDF_LHAID),
    }
    output_lines: list[str] = []
    for line in text.splitlines(keepends=True):
        for key, value in updates.items():
            pattern = re.compile(rf"^(\s*){re.escape(key)}(\s*=\s*).*$")
            if pattern.match(line.rstrip("\n")):
                newline = "\n" if line.endswith("\n") else ""
                line = pattern.sub(lambda match: f"{match.group(1)}{key}{match.group(2)}{value}", line.rstrip("\n")) + newline
                break
        output_lines.append(line)
    return "".join(output_lines)


def generate_run_card_include(process_dir: Path, cfg: Config) -> None:
    source_dir = process_dir / "Source"
    include_path = source_dir / "run_card.inc"
    if cfg.dry_run:
        print(f"+ make -C {source_dir} run_card.inc")
        return
    run(["make", "-C", source_dir, "run_card.inc"], cfg)
    if not include_path.exists():
        die(f"MG5 did not generate run card include: {include_path}")


def patch_run_card_include(process_dir: Path, cfg: Config) -> None:
    include_path = process_dir / "Source" / "run_card.inc"
    setrun_object = process_dir / "Source" / "setrun.o"
    if cfg.dry_run:
        print(f"+ patch generated MG5 include {include_path}")
        print(f"+ remove stale compiled setrun object {setrun_object} if present")
        return
    if setrun_object.exists():
        setrun_object.unlink()
    if not include_path.exists():
        return
    updated = rewrite_run_card_include(include_path.read_text())
    include_path.write_text(updated)


def patch_mg5_pdf_label_sync(process_dir: Path, cfg: Config) -> None:
    interface = process_dir / "bin" / "internal" / "madevent_interface.py"
    if cfg.dry_run:
        print(f"+ patch MG5 PDF label sync {interface}")
        return
    if not interface.exists():
        die(f"missing MG5 generated interface: {interface}")

    text = interface.read_text()
    if MG5_PDF_LABEL_SYNC_MARKER in text:
        return

    needle = "        # set  lhapdf.\n"
    if needle not in text:
        die(f"could not locate MG5 LHAPDF setup block in {interface}")

    block = (
        f"        {MG5_PDF_LABEL_SYNC_MARKER}\n"
        "        if self.run_card['pdlabel1'] == \"lhapdf\" or self.run_card['pdlabel2'] == \"lhapdf\":\n"
        "            self.run_card['pdlabel'] = \"lhapdf\"\n"
    )
    interface.write_text(text.replace(needle, needle + block, 1))


def patch_mg5_pdf_defaults(process_dir: Path, cfg: Config) -> None:
    banner = process_dir / "bin" / "internal" / "banner.py"
    if cfg.dry_run:
        print(f"+ patch MG5 PDF hidden defaults {banner}")
        return
    if not banner.exists():
        die(f"missing MG5 generated banner helper: {banner}")

    replacements = {
        'self.add_param("pdlabel", "nn23lo1", hidden=True, allowed=valid_pdf)': (
            'self.add_param("pdlabel", "lhapdf", hidden=True, allowed=valid_pdf)'
        ),
        'self.add_param("pdlabel1", "nn23lo1", hidden=True, allowed=valid_pdf, fortran_name="pdsublabel(1)")': (
            'self.add_param("pdlabel1", "lhapdf", hidden=True, allowed=valid_pdf, fortran_name="pdsublabel(1)")'
        ),
        'self.add_param("pdlabel2", "nn23lo1", hidden=True, allowed=valid_pdf, fortran_name="pdsublabel(2)")': (
            'self.add_param("pdlabel2", "lhapdf", hidden=True, allowed=valid_pdf, fortran_name="pdsublabel(2)")'
        ),
        'self.add_param("lhaid", 230000, hidden=True)': (
            f'self.add_param("lhaid", {LO_PDF_LHAID}, hidden=True)'
        ),
    }
    text = banner.read_text()
    updated = text
    for old, new in replacements.items():
        if old in updated:
            updated = updated.replace(old, new, 1)
        elif new not in updated:
            die(f"could not locate MG5 hidden PDF default in {banner}: {old}")
    if updated != text:
        banner.write_text(updated)


def write_herwig_input(sample: Sample, lhe_file: Path, seed: int, output: Path, cfg: Config) -> None:
    if cfg.dry_run:
        print(f"+ write Herwig input {output}")
        print(f"replace LHEFILE.lhe.gz -> {lhe_file}")
        print(f"set theGenerator:NumberOfEvents {cfg.nevents}")
        print(f"set theGenerator:RandomNumberGenerator:Seed {seed}")
        print(f"set /Herwig/Partons/thePDFset:PDFName {cfg.herwig_pdf}")
        print(f"saverun {sample.name} theGenerator")
        return

    text = cfg.template_in.read_text()
    text = text.replace("LHEFILE.lhe.gz", str(lhe_file))
    text = re.sub(r"set theGenerator:NumberOfEvents\s+\S+", f"set theGenerator:NumberOfEvents {cfg.nevents}", text)
    text = re.sub(
        r"set theGenerator:RandomNumberGenerator:Seed\s+\S+",
        f"set theGenerator:RandomNumberGenerator:Seed {seed}",
        text,
    )
    text = re.sub(
        r"set /Herwig/Partons/thePDFset:PDFName\s+\S+",
        f"set /Herwig/Partons/thePDFset:PDFName {cfg.herwig_pdf}",
        text,
    )
    text = re.sub(
        r"set /Herwig/Analysis/HwSim:OutputLocation\s+\S+",
        "set /Herwig/Analysis/HwSim:OutputLocation events/",
        text,
    )
    text = re.sub(r"saverun\s+\S+\s+theGenerator", f"saverun {sample.name} theGenerator", text)
    output.write_text(text)


def shared_library_glob(system: str) -> str:
    return "*.dylib" if system == "Darwin" else "*.so*"


def copy_library(src: Path, dest: Path) -> None:
    tmp = dest.with_name(f"{dest.name}.tmp.{os.getpid()}")
    shutil.copy2(src, tmp, follow_symlinks=True)
    tmp.replace(dest)


def prepare_collier_runtime(runtime_lib_dir: Path, cfg: Config) -> None:
    if cfg.collier_library is None:
        return

    source_lib_dir = cfg.collier_library.parent
    ensure_dir(runtime_lib_dir, cfg)
    system = platform.system()
    pattern = shared_library_glob(system)

    if cfg.dry_run:
        print(f"+ cp -pL {q(source_lib_dir / pattern)} {q(str(runtime_lib_dir) + '/')}")
        if system == "Darwin":
            print(f"+ patch OpenLoops dylib install names in {q(runtime_lib_dir)}")
        return

    for lib in sorted(source_lib_dir.glob(pattern)):
        if lib.is_file() or lib.is_symlink():
            copy_library(lib, runtime_lib_dir / lib.name)

    collier_alias = "libcollier.dylib" if system == "Darwin" else "libcollier.so"
    alias_path = runtime_lib_dir / collier_alias
    if not alias_path.exists():
        copy_library(cfg.collier_library, alias_path)

    if system == "Darwin":
        patch_openloops_dylibs(runtime_lib_dir)


def run_optional_tool(args: Sequence[object]) -> subprocess.CompletedProcess[str] | None:
    if shutil.which(str(args[0])) is None:
        return None
    return subprocess.run([str(arg) for arg in args], text=True, stdout=subprocess.PIPE, stderr=subprocess.DEVNULL)


def patch_openloops_dylibs(runtime_lib_dir: Path) -> None:
    """Make copied OpenLoops dylibs independent of the current working directory.

    The local macOS build uses install names like ``lib/libolcommon...``.  MG5
    runs helper binaries from several directories, so patch the copied dylibs to
    resolve sibling OpenLoops libraries via @loader_path.
    """

    if shutil.which("install_name_tool") is None or shutil.which("otool") is None:
        return

    dylibs = sorted(runtime_lib_dir.glob("*.dylib"))
    for dylib in dylibs:
        subprocess.run(["install_name_tool", "-id", f"@rpath/{dylib.name}", str(dylib)], stderr=subprocess.DEVNULL)

    for dylib in dylibs:
        result = run_optional_tool(["otool", "-L", dylib])
        if result is None:
            continue
        for raw_line in result.stdout.splitlines()[1:]:
            dep = raw_line.strip().split(maxsplit=1)[0]
            if not dep.startswith("lib/") or not dep.endswith(".dylib"):
                continue
            dep_name = Path(dep).name
            if (runtime_lib_dir / dep_name).exists():
                subprocess.run(
                    ["install_name_tool", "-change", dep, f"@loader_path/{dep_name}", str(dylib)],
                    stderr=subprocess.DEVNULL,
                )


def inject_mg5_runtime_rpath(process_dir: Path, runtime_lib_dir: Path | None, cfg: Config) -> None:
    if runtime_lib_dir is None:
        return
    make_opts = process_dir / "Source" / "make_opts"
    block = (
        "\n# gamma-gamma campaign runtime library path\n"
        f"LDFLAGS += -Wl,-rpath,{runtime_lib_dir}\n"
        f"RPATH_LIBS += -Wl,-rpath,{runtime_lib_dir}\n"
    )
    if cfg.dry_run:
        print(f"+ patch {make_opts}")
        print(block.strip())
        return
    if not make_opts.exists():
        die(f"missing MG5 make options: {make_opts}")
    text = make_opts.read_text()
    if "# gamma-gamma campaign runtime library path" not in text:
        make_opts.write_text(text.rstrip() + block)


def link_madloop_runtime_dirs(process_dir: Path, runtime_lib_dir: Path | None, cfg: Config) -> None:
    if runtime_lib_dir is None:
        return
    subproc_root = process_dir / "SubProcesses"
    if cfg.dry_run:
        print(f"+ link {runtime_lib_dir} as lib/ under {subproc_root}/P*")
        return
    for subproc_dir in sorted(subproc_root.glob("P*")):
        if not subproc_dir.is_dir():
            continue
        link = subproc_dir / "lib"
        if link.is_symlink():
            link.unlink()
        if not link.exists():
            link.symlink_to(runtime_lib_dir, target_is_directory=True)


def restore_mg5_symmetry_factors(process_dir: Path, cfg: Config) -> None:
    subproc_root = process_dir / "SubProcesses"
    if cfg.dry_run:
        print(f"+ restore missing symfact.dat from symfact_orig.dat under {subproc_root}/P*")
        return
    for subproc_dir in sorted(subproc_root.glob("P*")):
        symfact = subproc_dir / "symfact.dat"
        symfact_orig = subproc_dir / "symfact_orig.dat"
        if not symfact.exists() and symfact_orig.exists():
            shutil.copy2(symfact_orig, symfact)


def generation_environment(runtime_lib_dir: Path | None) -> dict[str, str] | None:
    if runtime_lib_dir is None:
        return None

    env = os.environ.copy()
    system = platform.system()
    if system == "Darwin":
        prepend_env_path(env, "DYLD_LIBRARY_PATH", runtime_lib_dir)
        prepend_env_path(env, "DYLD_FALLBACK_LIBRARY_PATH", runtime_lib_dir)
    else:
        prepend_env_path(env, "LD_LIBRARY_PATH", runtime_lib_dir)
    return env


def prepend_env_path(env: dict[str, str], key: str, path: Path) -> None:
    current = env.get(key)
    env[key] = str(path) if not current else f"{path}{os.pathsep}{current}"


def run_generate_events(process_dir: Path, runtime_lib_dir: Path | None, cfg: Config) -> None:
    args = [process_dir / "bin" / "generate_events", cfg.run_tag, "-f", f"--nb_core={cfg.nb_core}"]
    run(args, cfg, cwd=process_dir, env=generation_environment(runtime_lib_dir))


def run_herwig(cwd: Path, cfg: Config, args: Sequence[object]) -> None:
    run_runtime_command([cfg.herwig, *args], cfg, cwd=cwd)


def find_lhe_file(process_dir: Path, prefer_decayed: bool) -> Path:
    candidates = sorted((process_dir / "Events").glob("*/unweighted_events.lhe*"))
    if prefer_decayed:
        for candidate in candidates:
            if "decayed" in str(candidate):
                return candidate
    if candidates:
        return candidates[-1]
    die(f"could not locate MG5 LHE output in {process_dir}")


def write_root_input_list(herwig_events_dir: Path, root_input: Path, cfg: Config) -> None:
    if cfg.dry_run:
        print(f"+ find {herwig_events_dir} -type f -name '*.root' > {root_input}")
        return
    roots = sorted(herwig_events_dir.glob("**/*.root"))
    if not roots:
        die(f"no HwSim ROOT files found in {herwig_events_dir}")
    root_input.write_text("".join(f"{root}\n" for root in roots))


def run_sample(index: int, sample: Sample, cfg: Config) -> None:
    seed = cfg.seed_base + index
    sample_dir = SCRIPT_DIR / "LOAnalysis" / sample.category / "events" / sample.name
    card_dir = sample_dir / "cards"
    mg5_process_dir = sample_dir / "mg5_process"
    herwig_dir = sample_dir / "herwig"
    herwig_events_dir = herwig_dir / "events"
    root_input = sample_dir / f"{sample.name}_hwsim_roots.input"
    mg5_card = card_dir / f"{sample.name}_proc_card.dat"
    herwig_input = herwig_dir / f"{sample.name}.in"

    log(f"Running sample {sample.name}")
    print(
        f"category={sample.category} model={sample.model} process={sample.process} "
        f"weight_scale={sample.weight_scale} seed={seed}"
    )

    ensure_dir(card_dir, cfg)
    ensure_dir(herwig_events_dir, cfg)

    runtime_lib_dir: Path | None = None
    if cfg.collier_library is not None:
        runtime_lib_dir = sample_dir / "lib"
        prepare_collier_runtime(runtime_lib_dir, cfg)

    write_mg5_process_card(mg5_card, sample, mg5_process_dir, cfg)
    run(mg5_command(cfg, mg5_card), cfg)

    inject_mg5_runtime_rpath(mg5_process_dir, runtime_lib_dir, cfg)
    link_madloop_runtime_dirs(mg5_process_dir, runtime_lib_dir, cfg)
    restore_mg5_symmetry_factors(mg5_process_dir, cfg)

    patch_mg5_pdf_defaults(mg5_process_dir, cfg)
    patch_mg5_pdf_label_sync(mg5_process_dir, cfg)
    patch_run_card(mg5_process_dir / "Cards" / "run_card.dat", seed, cfg)
    generate_run_card_include(mg5_process_dir, cfg)
    patch_run_card_include(mg5_process_dir, cfg)
    if sample.madspin_decay:
        write_madspin_card(mg5_process_dir / "Cards" / "madspin_card.dat", sample.madspin_decay, cfg)

    run_generate_events(mg5_process_dir, runtime_lib_dir, cfg)

    if cfg.dry_run:
        if sample.madspin_decay:
            lhe_file = mg5_process_dir / "Events" / f"{cfg.run_tag}_decayed_1" / "unweighted_events.lhe.gz"
        else:
            lhe_file = mg5_process_dir / "Events" / cfg.run_tag / "unweighted_events.lhe.gz"
    else:
        lhe_file = find_lhe_file(mg5_process_dir, prefer_decayed=bool(sample.madspin_decay))

    write_herwig_input(sample, lhe_file, seed, herwig_input, cfg)
    run_herwig(herwig_dir, cfg, ["read", herwig_input])
    run_herwig(herwig_dir, cfg, ["run", f"{sample.name}.run", f"-N{cfg.nevents}"])

    write_root_input_list(herwig_events_dir, root_input, cfg)
    run_runtime_command([cfg.analysis_exe, root_input, "-t", cfg.run_tag, "-w", sample.weight_scale], cfg)


def main(argv: Sequence[str]) -> int:
    cfg = parse_config(argv)
    require_inputs(cfg)

    log("Building gamma-gamma post-analysis")
    run_runtime_command(["make", "-C", cfg.analysis_code_dir, "HwSimPostAnalysis_gammagamma"], cfg)

    selected = [sample for sample in SAMPLES if sample_requested(sample, cfg)]
    if not selected:
        die(f"no samples selected by RUN_SAMPLES={cfg.run_samples}")

    for index, sample in enumerate(selected):
        run_sample(index, sample, cfg)

    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
