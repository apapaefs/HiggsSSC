def env_text(name: str, default: str) -> str:
    return os.environ.get(name, default)


def env_int(name: str, default: int) -> int:
    return int(os.environ.get(name, str(default)))


def env_float(name: str, default: float) -> float:
    return float(os.environ.get(name, str(default)))


def env_bool(name: str, default: bool) -> bool:
    value = os.environ.get(name)
    if value is None:
        return default
    return value.lower() in {"1", "true", "yes", "on"}


def maybe_path(value: str | None) -> Path | None:
    if not value:
        return None
    return Path(value).expanduser()


def guess_default_herwig_env() -> Path | None:
    default = maybe_path(os.environ.get("DEFAULT_HERWIG_ENV"))
    if default and default.exists():
        return default
    candidate = DEFAULT_HERWIG_PREFIX / "bin" / "activate"
    return candidate if candidate.exists() else None


def clean_optional_text(value: str | None) -> str | None:
    if value is None:
        return None
    value = value.strip()
    return value or None


def guess_collier_library(herwig_env: Path | None) -> Path | None:
    explicit = os.environ.get("COLLIER_LIBRARY") or os.environ.get("COLLIER_DYLIB")
    if explicit:
        return Path(explicit).expanduser()

    prefix = DEFAULT_HERWIG_PREFIX
    if herwig_env and herwig_env.name == "activate":
        prefix = herwig_env.parent.parent

    system = platform.system()
    if system == "Darwin":
        fixed_candidates = [
            prefix / "opt" / "OpenLoops-2.1.4" / "lib" / "libcollier.2.dylib",
            prefix / "opt" / "OpenLoops-2.1.4" / "lib" / "libcollier.dylib",
        ]
        glob_pattern = "libcollier*.dylib"
    else:
        fixed_candidates = [
            prefix / "opt" / "OpenLoops-2.1.4" / "lib" / "libcollier.so",
            prefix / "opt" / "OpenLoops-2.1.4" / "lib" / "libcollier.so.2",
        ]
        glob_pattern = "libcollier.so*"

    for candidate in fixed_candidates:
        if candidate.exists():
            return candidate

    opt_dir = prefix / "opt"
    if opt_dir.exists():
        for candidate in sorted(opt_dir.glob(f"OpenLoops-*/lib/{glob_pattern}")):
            if candidate.exists():
                return candidate
    return None


